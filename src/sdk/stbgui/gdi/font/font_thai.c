BOOL get_thai_char_metrics(U16 index, GS_TT_OUTLINE *outline, int *ascender, int *advance)
{
	int count = 0;

	if (index >= THAI_CHAR_NUMBER)
	{
		return FALSE;
	}
	if (outline != NULL)
	{
		outline->ucContoursNum = thai_char_metrics[index].metrics.contours_num;
		outline->ucPointsNum = thai_char_metrics[index].metrics.points_num;
		for (count = 0; count < outline->ucPointsNum && count < FONT_MAX_POINT; count++)
		{
			outline->astPoints[count].x = thai_char_vector[thai_char_metrics[index].metrics.points_addr + count].x;
			outline->astPoints[count].y = thai_char_vector[thai_char_metrics[index].metrics.points_addr + count].y;
		}
		outline->acTags = thai_char_tags + thai_char_metrics[index].metrics.tags_addr;
		outline->acContours = thai_char_contours + thai_char_metrics[index].metrics.contours_addr;
	}
	if (ascender != NULL)
	{
		*ascender = THAI_CHAR_ASCENDER;
	}
	if (advance != NULL)
	{
		*advance = thai_char_metrics[index].advance;
	}
	return TRUE;
}
