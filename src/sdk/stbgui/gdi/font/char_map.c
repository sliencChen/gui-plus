static GetMetricsFunc find_get_char_metrics_func(U16 char_code)
{
	int char_map_num;
	int low, high, mid;

	char_map_num = sizeof(font_char_func_map) / sizeof(font_char_func_map[0]);
	low = 0;
	high = char_map_num - 1;

	while (low <= high)
	{
		mid = (low + high) / 2;
		if (char_code >= font_char_func_map[mid].start_char_code && char_code <= font_char_func_map[mid].end_char_code)
		{
			return font_char_func_map[mid].get_metrics_func;
		}
		else if (char_code > font_char_func_map[mid].end_char_code)
		{
			low = mid + 1;
		}
		else if (char_code < font_char_func_map[mid].start_char_code)
		{
			high = mid - 1;
		}
	}
	return NULL;
}

