U16 get_unicode_glyph(U16 unicode_char)
{
	GetGlypFunc get_glyph_func;
	FONT_CHAR_OFFSET_MAP *offset;
	U16 glyph = 0;

	get_glyph_func = find_get_unicode_glyph_func(unicode_char);
	if (get_glyph_func)
	{
		offset = find_get_char_metrics_offset(unicode_char);
		if (offset)
		{
			glyph = get_glyph_func(unicode_char - offset->offset);
		}
	}
	else
	{
		glyph = unicode_char;
	}
	return glyph;
}

void get_unicode_font_glyph(U16 unicode_char, FONT_GLYPH *font_glyph)
{
	GetGlypFunc get_glyph_func = find_get_unicode_glyph_func(unicode_char);

	font_glyph->font_glyph_get_metrics_func = get_glyph_func ? find_get_char_metrics_func(unicode_char) : NULL;
	font_glyph->font_get_glyph_props_func = find_get_glyph_props_func(unicode_char);
	font_glyph->glyph = get_unicode_glyph(unicode_char);
}

static BOOL get_char_metrics_ex(U16 char_code, GS_TT_OUTLINE *outline, int *ascender, int *advance)
{
	GetMetricsFunc get_metrics_func;
	FONT_CHAR_OFFSET_MAP *offset;
	BOOL result = FALSE;

	get_metrics_func = find_get_char_metrics_func(char_code);
	if (get_metrics_func)
	{
		offset = find_get_char_metrics_offset(char_code);
		if (offset)
		{
			result = get_metrics_func(char_code - offset->offset, outline, ascender, advance);
		}
	}
	return result;
}

void get_char_metrics(FONT_GLYPH font_glyph, GS_TT_OUTLINE *outline, int *ascender, int *advance)
{
	GetMetricsFunc font_glyph_get_metrics_func;
	BOOL result = FALSE;

	font_glyph_get_metrics_func = font_glyph.font_glyph_get_metrics_func;
	if (font_glyph_get_metrics_func)
	{
		result = font_glyph_get_metrics_func(font_glyph.glyph, outline, ascender, advance);
	}
	else
	{
		result = get_char_metrics_ex(font_glyph.glyph, outline, ascender, advance);
	}

	if (!result)
	{
		get_none_char_metrics(outline, ascender, advance);
	}
}

